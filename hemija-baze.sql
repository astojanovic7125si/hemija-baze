    COUNT(s.id_sesija) AS broj_sesija
FROM istrazivac i
JOIN izvodjac iz
    ON i.id_istrazivac = iz.id_istrazivac
JOIN izvodjenje iv
    ON iz.id_izvodjenje = iv.id_izvodjenje
JOIN eksperiment e
    ON iv.id_eksperiment = e.id_eksperiment
JOIN laboratorija l
    ON iv.id_lab = l.id_lab
JOIN sesija s
    ON s.id_izvodjenje = iv.id_izvodjenje
GROUP BY
    i.id_istrazivac,
    i.ime,
    i.prezime,
    e.naziv,
    e.tip,
    l.naziv,
    iv.status
HAVING COUNT(s.id_sesija) >= 1;

