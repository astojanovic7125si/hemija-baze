-- Procedura: obrisi_sesiju_istrazivaca
-- Procedura proverava da li određeni istraživač učestvuje u izvođenju
-- za koje je zakazana sesija. Ako učestvuje, sesija se briše, a status
-- izvođenja se menja u 'otkazano'. Uvodi se da podrži formu za brisanje
-- sesije iz ugla istraživača.
-- Transakcija se koristi da bi se obe izmene izvršile zajedno ili nijedna.

DELIMITER $$
CREATE PROCEDURE obrisi_sesiju_istrazivaca(
    IN p_id_sesija INT,
    IN p_id_istrazivac INT
)
BEGIN
    DECLARE v_id_izvodjenje INT;
    DECLARE v_broj INT;

    DECLARE EXIT HANDLER FOR SQLEXCEPTION
    BEGIN
        ROLLBACK;
        SELECT 'Doslo je do greske. Transakcija je ponistena.' AS poruka;
    END;

    START TRANSACTION;

    SELECT id_izvodjenje
    INTO v_id_izvodjenje
    FROM sesija
    WHERE id_sesija = p_id_sesija;

    SELECT COUNT(*)
    INTO v_broj
    FROM izvodjac
    WHERE id_izvodjenje = v_id_izvodjenje
      AND id_istrazivac = p_id_istrazivac;

    IF v_broj > 0 THEN

        DELETE FROM sesija
        WHERE id_sesija = p_id_sesija;

        UPDATE izvodjenje
        SET status = 'otkazano'
        WHERE id_izvodjenje = v_id_izvodjenje;

        COMMIT;

        SELECT 'Sesija je uspesno obrisana.' AS poruka;

    ELSE

        ROLLBACK;

        SELECT 'Brisanje nije dozvoljeno: istrazivac ne ucestvuje u toj sesiji.' AS poruka;

    END IF;

END $$
DELIMITER ;
