-- Funkcija: broj_sesija_istrazivaca
-- Vraća broj sesija u kojima učestvuje određeni istraživač.
-- Uvodi se da bi aplikacija mogla brzo da prikaže opterećenje istraživača
-- kroz broj zakazanih ili realizovanih sesija.
DELIMITER $$
CREATE FUNCTION broj_sesija_istrazivaca(
    p_id_istrazivac INT
)
RETURNS INT
DETERMINISTIC
BEGIN
    DECLARE v_broj_sesija INT;

    SELECT COUNT(s.id_sesija)
    INTO v_broj_sesija
    FROM izvodjac iz
    JOIN sesija s
        ON iz.id_izvodjenje = s.id_izvodjenje
    WHERE iz.id_istrazivac = p_id_istrazivac;

    RETURN v_broj_sesija;
END $$
DELIMITER ;
