DROP DATABASE IF exists hemija;
CREATE DATABASE hemija;

USE hemija;

CREATE TABLE laboratorija(
	id_lab INT NOT NULL AUTO_INCREMENT,
    naziv VARCHAR(100) NOT NULL,
    lokacija VARCHAR(100) NOT NULL,
    specijalizacija VARCHAR(100) NOT NULL,
    
    PRIMARY KEY(id_lab)
);

CREATE TABLE resurs(
	id_resurs INT NOT NULL AUTO_INCREMENT,
    naziv VARCHAR(100) NOT NULL,
    opis VARCHAR(100) NOT NULL,
    agregatno_stanje VARCHAR(100) NOT NULL,
    reaktivnost VARCHAR(100),
    opasnost VARCHAR(100),
    
    PRIMARY KEY(id_resurs)
);

CREATE TABLE lab_resurs(
	id_lab INT NOT NULL,
    id_resurs INT NOT NULL,
    kolicina DECIMAL(10,2) NOT NULL,
    
    PRIMARY KEY(id_lab, id_resurs),
    
    FOREIGN KEY(id_lab)
		REFERENCES laboratorija(id_lab)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
        
	FOREIGN KEY(id_resurs)
		REFERENCES resurs(id_resurs)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE tip_alata(
	id_tip INT NOT NULL AUTO_INCREMENT,
    naziv VARCHAR(100) NOT NULL,
    opis VARCHAR(100) NOT NULL,
    
    PRIMARY KEY(id_tip)
);

CREATE TABLE alat(
	id_alat INT NOT NULL AUTO_INCREMENT,
    id_tip INT NOT NULL,
	id_lab INT NOT NULL,
	datum_nabavke DATE NOT NULL,
    datum_proizvodnje DATE,
    
    PRIMARY KEY(id_alat),
    
    FOREIGN KEY(id_tip)
		REFERENCES tip_alata(id_tip)
		ON UPDATE CASCADE
        ON DELETE RESTRICT,
        
    FOREIGN KEY(id_lab)
		REFERENCES laboratorija(id_lab)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE istrazivac(
	id_istrazivac INT NOT NULL AUTO_INCREMENT,
    ime VARCHAR(100) NOT NULL,
    prezime VARCHAR(100) NOT NULL,
    kvalifikacija VARCHAR(100) NOT NULL,
    
    vrsta ENUM('dizajner','izvodjac','oba') NOT NULL,
    
    PRIMARY KEY(id_istrazivac)
);

CREATE TABLE teorija(
	id_teorija INT NOT NULL AUTO_INCREMENT,
    naziv VARCHAR(100) NOT NULL,
    identifikator VARCHAR(100) NOT NULL,
    opis VARCHAR(100) NOT NULL,
    
    PRIMARY KEY(id_teorija)
);

CREATE TABLE eksperiment(
	id_eksperiment INT NOT NULL AUTO_INCREMENT,
    id_teorija INT NOT NULL,
    naziv VARCHAR(100) NOT NULL,
    tip VARCHAR(100) NOT NULL,
    cilj VARCHAR(100) NOT NULL,
    
    PRIMARY KEY(id_eksperiment),
    
    FOREIGN KEY(id_teorija)
		REFERENCES teorija(id_teorija)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE eksperiment_resurs(
	id_eksperiment INT NOT NULL,
    id_resurs INT NOT NULL,
    
    PRIMARY KEY(id_eksperiment, id_resurs),
    
    FOREIGN KEY(id_eksperiment)
		REFERENCES eksperiment(id_eksperiment)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
        
	FOREIGN KEY(id_resurs)
		REFERENCES resurs(id_resurs)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE eksperiment_alat(
	id_eksperiment INT NOT NULL,
    id_alat INT NOT NULL,
    
    PRIMARY KEY(id_eksperiment, id_alat),
    
    FOREIGN KEY(id_eksperiment)
		REFERENCES eksperiment(id_eksperiment)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
        
	FOREIGN KEY(id_alat)
		REFERENCES alat(id_alat)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE dizajner(
	id_eksperiment INT NOT NULL,
    id_istrazivac INT NOT NULL,
   
    PRIMARY KEY(id_eksperiment, id_istrazivac),
   
    FOREIGN KEY(id_eksperiment)
		REFERENCES eksperiment(id_eksperiment)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
        
	FOREIGN KEY(id_istrazivac)
		REFERENCES istrazivac(id_istrazivac)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE izvodjenje(
	id_izvodjenje INT NOT NULL AUTO_INCREMENT,
    id_eksperiment INT NOT NULL,
	id_lab INT NOT NULL,
    datum DATE NOT NULL,
    
    status ENUM(
				'planirano',
				'zapoceto',
                'otkazano',
                'zavrseno_uspesno',
                'zavrseno_neuspesno'
                ) NOT NULL,
    
   PRIMARY KEY(id_izvodjenje),
   
   FOREIGN KEY(id_eksperiment)
		REFERENCES eksperiment(id_eksperiment)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
        
	FOREIGN KEY(id_lab)
		REFERENCES laboratorija(id_lab)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE izvodjac(
	id_izvodjenje INT NOT NULL,
    id_istrazivac INT NOT NULL,
	uloga VARCHAR(100) NOT NULL,
    putanja_belesaka VARCHAR(255),
    
    PRIMARY KEY(id_izvodjenje, id_istrazivac),
   
    FOREIGN KEY(id_izvodjenje)
		REFERENCES izvodjenje(id_izvodjenje)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
        
	FOREIGN KEY(id_istrazivac)
		REFERENCES istrazivac(id_istrazivac)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE sesija(
	id_sesija INT NOT NULL AUTO_INCREMENT,
	id_izvodjenje INT NOT NULL,
	datum DATE NOT NULL,
    
    vreme_pocetka TIME NOT NULL,
    vreme_zavrsetka TIME NOT NULL,
    
    PRIMARY KEY(id_sesija),
   
    FOREIGN KEY(id_izvodjenje)
		REFERENCES izvodjenje(id_izvodjenje)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);
